using System;

namespace Classes.Math
{
    public class Calculator
    {
        public int Add(int a, int b)
        {
           return (a + b);
        }

        public int Subtract(int c, int d)
        {
            return (c - d);
        }

        public int Multiply(int e, int f)
        {
            return (e * f);
        }

        public double Divide(double g, double h)
        {
            return (g / h);
        }
    }
}
